using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace CampusDemo.ContinuousIntegration.SimpleLibrary
{
    public class Math
    {
        public int Sum(int value1, int value2)
        {
            return value1 + value2;
        }

        public int Difference(int value1, int value2)
        {
            return value1 - value2;
        }
    }
}
